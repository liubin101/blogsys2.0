package com.lb.entity;

public class ArtLike {
    private Long id;

    private Long artid;

    private Long userid;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getArtid() {
        return artid;
    }

    public void setArtid(Long artid) {
        this.artid = artid;
    }

    public Long getUserid() {
        return userid;
    }

    public void setUserid(Long userid) {
        this.userid = userid;
    }
}